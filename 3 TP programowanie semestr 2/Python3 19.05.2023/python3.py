#Tworzenie zmiennych z wartościami 5
x = 5
y = 5

#Wyświetlenie zmiennych czy zmienna x jest równa 5
print(x == y)

#Wyświetlenie zmiennych czy zmienna x jest róna od zmiennej y
print(x != y)

#Wyświetlenie zmiennych czy zmienna x jest większa od y
print(x > y)

#Wyświetlenie zmiennych czy zmienna x jest mniejsza od y
print(x < y)

"""
    Python posiada następujące operatory porównania:
    (>, <, >=, <=, ==, !=)
    > większe
    < mniejsze
    >= większe lub równe
    <= mniejsze lub równe
    == równe
    != rózne
"""